#los siguientes ejemplos de código imprimiría el tipo de datos de x, ¿qué tipo de datos sería?
x = 5
print(type(x))
#Tipo entero


x = "Hello world"
print(type(x))
#Tipo string


x = 20.5
print(type(x))
#Tipo float


x = ["apple", "banana", "cherry"]
print(type(x))
#Tipo lista o vector o arreglo


x = ("apple", "banana", "cherry")
print(type(x))
#Tipo tuple ->No se puede modificar el valor


x = {"name" : "jhon", "age" : 36}
print(type(x))
#Tipo dict -> Un diccionario con variables y valores definidos


x = True
print(type(x))
#Tipo booleano

